package main.java;

public class Surface {
    public double xmin;
    public double ymin;
    public double xmax;
    public double ymax;

    public Surface(float xmin, float ymin, float xmax, float ymax) {
        this.xmin = xmin;
        this.ymin = ymin;
        this.xmax = xmax;
        this.ymax = ymax;
    }
}
